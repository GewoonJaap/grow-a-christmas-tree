import { getRandomElement } from "./helpers/arrayHelper";

export function getRandomEmoji(): string {
  return getRandomElement(EMOJIS) ?? "😮‍💨";
}

export function getRandomEmojiWithExclusion(excluded: string[]): string {
  const filteredEmojis = EMOJIS.filter((emoji) => !excluded.includes(emoji));
  return getRandomElement(filteredEmojis) ?? "😮‍💨";
}

